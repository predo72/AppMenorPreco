class Item {
  final String id;
  final String idLista;
  final String nome;
  final String codBarras;
  final int quantidade;
  final String nomeMercado;
  final double valor;
  final int marcado;


  const Item({
    required this.id,
    required this.idLista,
    required this.nome,
    required this.codBarras,
    required this.quantidade,
    required this.nomeMercado,
    required this.valor,
    required this.marcado,
  });
}
